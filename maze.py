def shortestPath(maze, start, destination):
	class node(object):

		def __init__(self, x, y):
			self.x = x
			self.y = y
			self.parent = None

		def getPos(self):
			return (self.x, self.y)

	MAX_VAL = 9999999999

	m = len(maze)
	n = len(maze[0])
	stack = []

	stack.append(node(start[0], start[1]))

	distance = []
	path = []
	for i in range(m):
		distance.append([])
		path.append([])
		for j in range(n):
			distance[i].append(MAX_VAL)
			path[i].append((0, 0))

	distance[start[0]][start[1]] = 0

	visited = set()

	while stack:

		now_node = stack.pop(0)

		i, j = now_node.getPos()

		if i == destination[0] and j == destination[1]:

			return now_node

		visited.add((i,j))
		for u, v in [(0, 1), (1, 0), (-1, 0), (0, -1)]:
			temp_i = i
			temp_j = j

			temp_i += u
			temp_j += v
			if temp_i == i and temp_j == j:
				continue
			if 0 <= temp_i < m and 0 <= temp_j < n and maze[temp_i][temp_j] == 0 and (temp_i,temp_j) not in visited:
				new_node = node(temp_i, temp_j)
				new_node.parent = now_node
				stack.append(new_node)

	return -1



def bfs(arr, start, end):
	class node(object):

		def __init__(self, x, y):
			self.x = x
			self.y = y
			self.parent = None

		def getPos(self):
			return (self.x, self.y)

	queue = []
	x_len = len(arr)
	y_len = len(arr[0])
	visit = [[0 for _ in range(y_len)] for _ in range(x_len)]

	queue.append(node(start[0], start[1]))
	visit[start[0]][start[1]] = 1

	while queue:
		node_now = queue.pop(0)

		state = [node_now.x, node_now.y]

		for state_next in [[state[0] + 0, state[1] + 1],
						   [state[0] + 1, state[1] + 0],
						   [state[0] + 0, state[1] - 1],
						   [state[0] - 1, state[1] + 0]]:

			if state_next[0] < 0 or state_next[1] < 0 or state_next[0] >= x_len or state_next[1] >= y_len:
				continue

			node_next = node(state_next[0], state_next[1])
			node_next.parent = node_now

			if state_next == end:
				return node_next

			elif arr[state_next[0]][state_next[1]] == 0 and visit[state_next[0]][state_next[1]] == 0:
				queue.append(node_next)
				visit[state_next[0]][state_next[1]] = 1

	return False


